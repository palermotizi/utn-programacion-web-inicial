let dia = prompt ('introduce un numero del 1 al 7')
if (dia < 1 || dia > 7) {
    console.log('numero no valido')
} else if (dia == 1) {
    console.log('Lunes')
} else if (dia == 2) {
    console.log('Martes')
} else if (dia == 3) {
    console.log('Miercoles')
} else if (dia == 4) {
    console.log('Jueves')
} else if (dia == 5) {
    console.log('Viernes')
} else if (dia == 6) {
    console.log('Sabado')
} else if (dia == 7) {
    console.log('Domingo')
}